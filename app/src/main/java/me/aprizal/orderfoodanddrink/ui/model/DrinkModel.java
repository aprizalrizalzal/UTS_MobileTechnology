package me.aprizal.orderfoodanddrink.ui.model;

public class DrinkModel {
    private int img_drink;
    private String name_drink;
    private String price_drink;
    private String detail_drink;

    public int getImg_drink() {
        return img_drink;
    }

    public void setImg_drink(int img_drink) {
        this.img_drink = img_drink;
    }

    public String getName_drink() {
        return name_drink;
    }

    public void setName_drink(String name_drink) {
        this.name_drink = name_drink;
    }

    public String getPrice_drink() {
        return price_drink;
    }

    public void setPrice_drink(String price_drink) {
        this.price_drink = price_drink;
    }

    public String getDetail_drink() {
        return detail_drink;
    }

    public void setDetail_drink(String detail_drink) {
        this.detail_drink = detail_drink;
    }
}
