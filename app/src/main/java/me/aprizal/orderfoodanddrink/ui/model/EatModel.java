package me.aprizal.orderfoodanddrink.ui.model;

public class EatModel {
    private int img_eat;
    private String name_eat;
    private String price_eat;
    private String detail_eat;

    public int getImg_eat() {
        return img_eat;
    }

    public void setImg_eat(int img_eat) {
        this.img_eat = img_eat;
    }

    public String getName_eat() {
        return name_eat;
    }

    public void setName_eat(String name_eat) {
        this.name_eat = name_eat;
    }

    public String getPrice_eat() {
        return price_eat;
    }

    public void setPrice_eat(String price_eat) {
        this.price_eat = price_eat;
    }

    public String getDetail_eat() {
        return detail_eat;
    }

    public void setDetail_eat(String detail_eat) {
        this.detail_eat = detail_eat;
    }
}
